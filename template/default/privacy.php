Ai sensi e per gli effetti dell'art. 13 Codice della Privacy si rende noto quanto segue:<br /><br />



   1. I dati personali da Lei volontariamente forniti all'atto di compilazione del Modulo di Registrazione per l'Accesso al Servizio saranno oggetto di trattamento, che avrà luogo prevalentemente con modalità automatizzate ed informatizzate, sempre nel rispetto delle regole di riservatezza e di sicurezza previste dalla legge. I dati saranno conservati per i termini di legge presso SimpleNetworks srl e trattati da parte di dipendenti e/o professionisti da questa incaricati, i quali svolgono le suddette attività sotto la sua diretta supervisione e responsabilità. A tal fine, i dati comunicati dall'Utente potranno essere trasmessi a soggetti esterni che svolgono funzioni strettamente connesse e strumentali all'operatività del Servizio.<br /><br />



      Il trattamento dei dati ha finalità connesse o strumentali al Servizio fornito da SimpleNetworks srl, e precisamente:<br /><br />

          * raccogliere dati ed informazioni in via generale e particolare sugli orientamenti e le preferenze dell'Utente;<br />

          * inviare informazioni ed offerte commerciali, anche di terzi;<br />

          * inviare materiale pubblicitario e informativo;<br />

          * effettuare comunicazioni commerciali, anche interattive;<br />

          * compiere attività dirette di vendita o di collocamento di prodotti o servizi;<br />

          * elaborare studi e ricerche statistiche su vendite, clienti e altre informazioni, attività di marketing ed eventualmente comunicare le stesse a terze parti;<br />

          * cedere a terzi i dati raccolti ed elaborati a fini commerciali anche per la vendita o tentata vendita, ovvero per tutte quelle finalità a carattere commerciale e/o statistico lecite e già parzialmente indicate nei superiori punti;<br /><br />

   		  2. I dati personali potranno essere comunicati da SimpleNetworks srl alle categorie di soggetti di seguito esemplificate, per tutte le finalità indicate in precedenza al punto 1 della presente informativa:<br /><br />

