            Ai sensi dell'articolo 7 del D. Lgs. N. 196/2003, potrà esercitare i seguenti diritti:<br /><br />

               1. ottenere la conferma dell'esistenza dei dati personali che la riguardano, anche se non ancora registrati e la loro comunicazione in forma intelligibile;<br />

               2. ottenere l'indicazione delle origini dei dati personali, delle finalità e modalità del trattamento, della logica applicata in caso di trattamento effettuato con l'ausilio di strumenti elettronici, degli estremi identificativi del titolare e dei responsabili del trattamento e del rappresentante designato ai sensi dell'art. 5, co.2 del medesimo D. Lgs., dei soggetti o delle categorie di soggetti ai quali i dati personali possono essere comunicati o che possono venirne a conoscenza in qualità di rappresentante designato nel territorio dello Stato, di responsabili o incaricati;<br />

               3. ottenere l'aggiornamento, la rettificazione, ovvero, quando vi ha interesse, l'integrazione dei dati;<br />

               4. ottenere la cancellazione, la trasformazione, in forma anonima o il blocco dei dati trattati in violazione di legge, compresi quelli di cui non è necessaria la conservazione in relazione agli scopi per i quali i dati sono stati raccolti o successivamente trattati;
<br />
               5. ottenere l'attestazione che l'aggiornamento, la rettificazione, l'integrazione, la cancellazione, la trasformazione in forma anonima o il blocco sono stati portati a conoscenza, anche per quanto riguarda il loro contenuto, di coloro ai quali i dati sono stati comunicati o diffusi, eccettuato il caso in cui tale adempimento si rivela impossibile o comporta un impiego di mezzi manifestamente sproporzionato rispetto al diritto tutelato;<br />

               6. opporsi in tutto o in parte, per motivi legittimi, al trattamento dei dati personali che la riguardano, ancorchè pertinenti allo scopo della raccolta;<br />

